/*
* Ejercicio 2: Conversión a minúsculas
* Solicita al usuario que ingrese una palabra o frase utilizando prompt(). 
* Luego, muestra la entrada del usuario en minúsculas utilizando toLowerCase().
*/

let userInput = prompt("Ingrese una palabra o frase:");
let userInputLowerCase = userInput.toLowerCase();

alert("Entrada del usuario en minúsculas: " + userInputLowerCase);
